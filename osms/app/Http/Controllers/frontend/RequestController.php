    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(InfoRequest $request)
    {
      $requestData = $request->all();
      $requestData['user_id']= Auth::id();
      $userrequest=UserRequest::create($requestData);
      if($userrequest){
        return redirect()->route('request.show',$userrequest->id)->with('message', 'Your Request is sumited successfully please wait for our reponse.');
      }
      else{
        return redirect()->route('name')->with('error', 'Your Request is not sumited successfully for some technical issues.');
      }
    }

